    public ResultsFrame(PuzzleObserver obs,Set<String> correctWords, HashSet<String> possibleWords) {
        this.obs = obs;
        initComponents();
        this.setLocationRelativeTo(null);
        for(String s : correctWords){
            addUserWord(s,obs.getPoints(s));
        }
        for(String s : possibleWords){
            addPossibleWord(s,obs.getPoints(s));
        }
        setVisible(true);
        
    }


    /**
     * adds word entered by user during game
     *
     * @param s
     * @param points for string
     */
    public void addUserWord(String s, int points) {
        userWords.append(s.toLowerCase() + "\n");
        numberUserWords++;
        userPoints += points;
        userWordsLabel.setText("Words: " + numberUserWords);
        userPointsLabel.setText("Points: " + userPoints);
    }

    /**
     * adds word found by computer
     *
     * @param s
     * @param points for string
     */
    public void addPossibleWord(String s, int points) {
        possibleWords.append(s.toLowerCase() + "\n");
        numberPossibleWords++;
        possiblePoints += points;
        possibleWordsLabel.setText("Words: " + numberPossibleWords);
        possiblePointsLabel.setText("Points: " + possiblePoints);
    }

